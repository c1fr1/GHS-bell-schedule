class ScheduleTableViewCell : UITableViewCell, UITextFieldDelegate {
    @IBOutlet var classTextField : UITextField!
    @IBOutlet var roomTextField : UITextField!

    var info : PeriodInfo?

    func setup(info ii : PeriodInfo)
    {
        info = ii
        classTextField.text = ii.name
        roomTextField.text = ii.room
    }
    
    func textFieldShouldReturn(_ textField: UITextField) -> Bool {
        textField.resignFirstResponder()
        return true
    }
    
    func textFieldDidEndEditing(_ textField: UITextField) {
        if let contents = textField.text {
            if textField == classTextField {
                info?.name = contents
            } else {
                info?.room = contents
            }
        }
    }
}
